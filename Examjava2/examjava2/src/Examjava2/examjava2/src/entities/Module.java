package entities;

public class Module {
    private int idMod;
    private String nomMod;
    public Module() {
    }
    public int getIdMod() {
        return idMod;
    }
    public void setIdMod(int idMod) {
        this.idMod = idMod;
    }
    public String getNomMod() {
        return nomMod;
    }
    public void setNomMod(String nomMod) {
        this.nomMod = nomMod;
    }
}

